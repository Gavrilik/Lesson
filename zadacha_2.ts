const masChisel = [2, 5, 9, 15, 0, 4];
for (let i = 0; i <= masChisel.length; i++) {
  if (masChisel[i] > 3 && masChisel[i] < 10)
    console.log("числа больше 3 меньше 10", masChisel[i]);
}

